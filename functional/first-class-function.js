const add = function(a, b){
    return a + b 
}
const sub = function(a, b){
    return a - b
}
const mult = function(a, b){
    return a * b
}
const div = function (a, b){
    return a / b
}

const restDiv = (x, y) => x%y

console.log(add(10, 20))
console.log(sub(10, 50))
console.log(mult(80, 3))
console.log(div(9, 3))
